// while loop

let i = 1;
while(i <= 10){
    console.log(`Value of i : ${i}`);
    i += 2;
}

let myArray = ["flash", "batman", "superman"];

let idx = 0;

while(idx < myArray.length){
    console.log(`Value of ${myArray[idx]}`);
    idx += 1;
}

// do while loop

let score = 10;

do{
    console.log(`Score is : ${score}`);
    score += 1;
}while(score < 5);

